package cap3;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int a = 20;
		int b = 30;
		int c = 1;
		int d = 20;
		System.out.println("b < a: " + (b < a)); // Menor que
		System.out.println("a == d: " + (a == d)); // é Igual a
		System.out.println("c > b: " + (c > b)); // Maior que
		System.out.println("a != d: " + (a != d)); // Diferente de
		System.out.println("a <= d: " + (a <= d)); // Menor ou Igual a
		System.out.println("a >= b: " + (a >= b)); // Maior ou Igual a

	}
}
